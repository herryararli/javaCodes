package week3Tuesday;

public class Exapmle_2 {

	public static void main(String[] args) {
		
		// if key equals value, program will work
		// key : int, String and char
		
		int number = 100;
		
		switch (number) {
		case 1000:
			System.out.println("Case 1");
			break;
		case 10:
			System.out.println("Case 2");
			break;
		case 100:
			System.out.println("Case 3");
			break;
		default:
			System.out.println("No key value match");
		}
		

	}

}
