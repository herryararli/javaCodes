public class ConditionalOperator {

	public static void main(String[] args) {
		
		int num1, num2;
		num1 = 10;
		//condition : true - false
		num2 = (num1 > 9) ? 100 : 9 ;
		System.out.println(num2);
		
		num2 = (false) ? 30 : 11;
		System.out.println(num2);
		
		
		
		

	}

}
