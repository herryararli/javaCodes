package week3Thursday;

public class ForLoopExamples {
	public static void main(String[] args) {

		// in every step, increase the number and print value 
		
		
		//  0,1,2,3,4
		for(int i = 0; i<5 ; i++ ){
			System.out.print(i + " ");
		}
		
		System.out.println();
		
		// Write the table of 5 with for loop
		// 5*1, 5*2 , 5*3...
		
		for(int i = 1; i<=10; i++){
			System.out.println(5+"X"+i+" = "+5*i);
		}
		
		
	}
}
