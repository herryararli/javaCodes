package preBootCamp;

public class Class_6_Review {

	public static void main(String[] args) {
		// byte, short, int, long, float , double
		// Widening Casting 
		byte a = 18;
		
		short b = 900;
		
		int c = 900000;
		
		System.out.println((double)a);
		
		
		// Narrowing Casting
		double d = 42.98;
		int e = (int)d;
		
		// Widening Casting
		System.out.println((double)e);
		
	}

}
