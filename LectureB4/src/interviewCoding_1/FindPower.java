package interviewCoding_1;

public class FindPower {

	public static void main(String[] args) {
		// Find power of the integers
		// No built in methods
		
		int base = 5, power = 0;
		
		int result = 1;
		
		for (int i = 1; i <= power; i++) {
			result = result * base;
		}
		
		System.out.println(result);
		
		
	

	}

}
