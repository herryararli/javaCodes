package PreBootCamp;

public class PreClass3 {

	public static void main(String[] args) {
		
		// int can hold whole numbers
		int variable;
		variable = 90;
		
		// how to declare a variable in single line
		int variable1 = 99;

		// default value is zero;
		byte myByte = 0;
		// in order to use the variables in methods, we need to initialize the variable
		System.out.println(myByte);
		
		// Every character has Ascii numbers associated with them
		// Casting(changing data type) into char & integer
		System.out.println((char)100);
		System.out.println((int)'Q');
		System.out.println((int)9.999999f);
		
		// identifiers will start with (a-z, A-Z, $, _)
		int ac = 9;
		
		// reserved keywords can not be used in variable declaration
		// int int = 9;
		
	}
	

}
