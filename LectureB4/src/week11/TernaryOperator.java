package week11;


public class TernaryOperator {

	public static void main(String[] args) {
		
		int a,b;
		
		a = 10; 
		
		// variable = (boolean) ? value1 : value2
		
		b = (false) ? 20 : 30;
		
		System.out.println("Value of b is : " + b);
		
		
		b = (true || false) ? 40 : 57;
		
		System.out.println("Value of b is : " + b);
		
		

	}

}
