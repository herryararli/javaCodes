package week1;

public class RelationalOperators {

	public static void main(String[] args) {
		
		// ==, != , > , < , >= , <= , !
		
		System.out.println("First Expression : " + (90==8)); // false
		
		boolean myBoolean1 = (5!=5) ;
		System.out.println("Second Expression : " + myBoolean1);// false

		System.out.println("Third Expression : " + (7>9)); // false
		
		System.out.println("Fourth Expression : " + (6<99)); // True
		
		System.out.println("Fifth Expression : " + (90>=90)); // true
		
		System.out.println("Sixth Expression : " + (70<=90)); // true
		
		System.out.println("Seventh Expression : " + !(70<=90)); // true
		
		
		
		
	}

}
