package preBootCamp;

public class Example {

	public static void main(String[] args) {
		
		// And - Or (&&, ||)
		
		System.out.println((4==5) && true); // false
		
		boolean myBoolean = true;
		System.out.println((6!=8) && true && myBoolean); // true
		
		System.out.println(myBoolean || false || !(9==9)); // true
		
		boolean check = ("Ahmet"=="Ahmet") && 9>7;
		
		System.out.println(check);
		
		
		
		
		
		

	}

}
